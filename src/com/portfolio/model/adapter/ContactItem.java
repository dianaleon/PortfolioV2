package com.portfolio.model.adapter;

public class ContactItem {

	private String type;
	private String value;
	
	public ContactItem() {
		
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
	
}
